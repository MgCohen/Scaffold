using System.Collections.Generic;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;

namespace Scaffold.GraphFlow.PackageGenerator
{
    /// <summary>
    /// Builds the per-package registry partial and the per-payload registration snippets the baker
    /// reads to dispatch editor nodes → runtime nodes generically.
    /// <para>Post-M3 phase 2 (decision #4): port IDs are strings (field names). Registrations are
    /// HashSets of valid port names per role; the name is the id end-to-end.</para>
    /// </summary>
    internal static class GraphRegistryEmitter
    {
        internal const string EditorRegistryNamespace = "Scaffold.GraphFlow.Editor";
        internal const string PackageEditorGToolkitNamespace = "Scaffold.GraphFlow.Editor.GToolkit";
        internal const string PackageRuntimeNamespace = "Scaffold.GraphFlow";
        internal const string EditorRegistryTypeName = "GraphPackageRegistry";

        internal static string ResolveRegistryNamespace(GraphPackageModel package, Compilation compilation)
        {
            if (!string.IsNullOrEmpty(package.RegistryNamespace))
            {
                return package.RegistryNamespace;
            }

            return GraphCompilationNames.EditorPackageRoot(compilation) + ".Editor.Generated";
        }

        internal static string RegistryTypeName(GraphPackageModel package)
        {
            return package.GraphStem + "GraphRegistry";
        }

        internal static void EmitRegistryFile(
            SourceProductionContext spc,
            Compilation compilation,
            GraphPackageModel package,
            IReadOnlyList<string> registrationBlocks,
            IReadOnlyList<EventTypeDescriptor>? eventTypes = null)
        {
            var ns = ResolveRegistryNamespace(package, compilation);
            var typeName = RegistryTypeName(package);
            var runnerFq = TrimGlobal(package.RunnerFullyQualified);

            var sb = new StringBuilder();
            sb.AppendLine("// <auto-generated />");
            sb.AppendLine("#nullable enable");
            sb.AppendLine($"using {EditorRegistryNamespace};");
            sb.AppendLine($"using {PackageRuntimeNamespace};");
            sb.AppendLine();
            sb.AppendLine($"namespace {ns}");
            sb.AppendLine("{");
            sb.AppendLine($"    public static partial class {typeName}");
            sb.AppendLine("    {");
            sb.AppendLine($"        public static readonly {EditorRegistryTypeName}<{runnerFq}> Instance = Build();");
            sb.AppendLine();
            AppendEventTypesTable(sb, eventTypes);
            sb.AppendLine($"        static {EditorRegistryTypeName}<{runnerFq}> Build()");
            sb.AppendLine("        {");
            sb.AppendLine($"            var r = new {EditorRegistryTypeName}<{runnerFq}>();");
            sb.AppendLine("            RegisterEventTypes();");
            sb.AppendLine("            RegisterGenerated(r);");
            sb.AppendLine("            RegisterOnTrigger(r);");
            sb.AppendLine("            RegisterAdditional(r);");
            sb.AppendLine("            return r;");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("        static void RegisterEventTypes()");
            sb.AppendLine("        {");
            sb.AppendLine("            foreach (var meta in EventTypes)");
            sb.AppendLine("            {");
            sb.AppendLine("                GraphEventTypeRegistry.Register(meta);");
            sb.AppendLine("            }");
            sb.AppendLine("        }");
            sb.AppendLine();
            AppendOnTriggerRegistration(sb, runnerFq);
            sb.AppendLine($"        static partial void RegisterAdditional({EditorRegistryTypeName}<{runnerFq}> r);");
            sb.AppendLine();
            sb.AppendLine($"        static void RegisterGenerated({EditorRegistryTypeName}<{runnerFq}> r)");
            sb.AppendLine("        {");
            foreach (var block in registrationBlocks)
            {
                sb.Append(block);
            }

            sb.AppendLine("        }");
            sb.AppendLine("    }");
            sb.AppendLine("}");

            spc.AddSource($"{typeName}.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
        }

        static void AppendEventTypesTable(StringBuilder sb, IReadOnlyList<EventTypeDescriptor>? eventTypes)
        {
            sb.AppendLine("        public static readonly System.Collections.Generic.IReadOnlyList<EventTypeMeta> EventTypes = new EventTypeMeta[]");
            sb.AppendLine("        {");
            if (eventTypes != null)
            {
                foreach (var et in eventTypes)
                {
                    sb.AppendLine($"            new EventTypeMeta(typeof({et.TypeFq}), new EventPortMeta[]");
                    sb.AppendLine("            {");
                    foreach (var f in et.Fields)
                    {
                        sb.AppendLine($"                new EventPortMeta(\"{f.Name}\", typeof({f.TypeFq})),");
                    }
                    sb.AppendLine("            }),");
                }
            }
            sb.AppendLine("        };");
            sb.AppendLine();
        }

        static void AppendOnTriggerRegistration(StringBuilder sb, string runnerFq)
        {
            sb.AppendLine($"        static void RegisterOnTrigger({EditorRegistryTypeName}<{runnerFq}> r)");
            sb.AppendLine("        {");
            sb.AppendLine($"            r.Register(new {EditorRegistryTypeName}<{runnerFq}>.NodeRegistration");
            sb.AppendLine("            {");
            sb.AppendLine("                EditorNodeType = typeof(Scaffold.GraphFlow.Editor.Nodes.OnTriggerEditorNode),");
            sb.AppendLine("                Factory = node =>");
            sb.AppendLine("                {");
            sb.AppendLine("                    var typed = (Scaffold.GraphFlow.Editor.Nodes.OnTriggerEditorNode)node;");
            sb.AppendLine("                    var typeOpt = typed.GetNodeOptionByName(Scaffold.GraphFlow.Editor.Nodes.OnTriggerEditorNode.EventTypeOptionName);");
            sb.AppendLine("                    System.Type? eventType = null;");
            sb.AppendLine("                    if (typeOpt != null && typeOpt.TryGetValue<string>(out var aqn) && !string.IsNullOrEmpty(aqn))");
            sb.AppendLine("                    {");
            sb.AppendLine("                        eventType = System.Type.GetType(aqn, throwOnError: false);");
            sb.AppendLine("                    }");
            sb.AppendLine("                    if (eventType == null)");
            sb.AppendLine("                    {");
            sb.AppendLine("                        throw new System.InvalidOperationException(\"OnTrigger node has no EventType option set or the type cannot be resolved.\");");
            sb.AppendLine("                    }");
            sb.AppendLine("                    var closed = typeof(Scaffold.GraphFlow.OnTrigger<>).MakeGenericType(eventType);");
            sb.AppendLine("                    var inst = (Scaffold.GraphFlow.RuntimeNode)System.Activator.CreateInstance(closed)!;");
            sb.AppendLine("                    var timingOpt = typed.GetNodeOptionByName(Scaffold.GraphFlow.Editor.Nodes.OnTriggerEditorNode.TimingOptionName);");
            sb.AppendLine("                    if (timingOpt != null && timingOpt.TryGetValue<Scaffold.GraphFlow.Timing>(out var t))");
            sb.AppendLine("                    {");
            sb.AppendLine("                        closed.GetField(\"Timing\")!.SetValue(inst, t);");
            sb.AppendLine("                    }");
            sb.AppendLine("                    return inst;");
            sb.AppendLine("                },");
            sb.AppendLine("                FlowOutputPortNames = new System.Collections.Generic.HashSet<string>(System.StringComparer.Ordinal) { \"FlowOut\" },");
            sb.AppendLine("            });");
            sb.AppendLine("        }");
            sb.AppendLine();
        }

        /// <summary>Compile-time descriptor for one [GraphEvent]-tagged class — feeds the per-package
        /// EventTypes table emitted into the registry file.</summary>
        internal sealed class EventTypeDescriptor
        {
            public string TypeFq { get; }
            public IReadOnlyList<EventFieldDescriptor> Fields { get; }
            public EventTypeDescriptor(string typeFq, IReadOnlyList<EventFieldDescriptor> fields)
            {
                TypeFq = typeFq;
                Fields = fields;
            }
        }

        internal sealed class EventFieldDescriptor
        {
            public string Name { get; }
            public string TypeFq { get; }
            public EventFieldDescriptor(string name, string typeFq) { Name = name; TypeFq = typeFq; }
        }

        internal static string BuildEntryRegistrationBlock(
            string runnerFq,
            string editorTypeFq,
            string runtimeTypeFq,
            string payloadTypeFq,
            string flowOutPortName,
            IReadOnlyList<string> dataOutputs,
            IReadOnlyList<string>? extraFlowOutputs = null)
        {
            var sb = new StringBuilder();
            sb.AppendLine($"            r.Register(new {EditorRegistryTypeName}<{runnerFq}>.NodeRegistration");
            sb.AppendLine("            {");
            sb.AppendLine($"                EditorNodeType = typeof({editorTypeFq}),");
            sb.AppendLine($"                Factory = _ => new {runtimeTypeFq}(),");
            sb.AppendLine("                FlowOutputPortNames = new System.Collections.Generic.HashSet<string>(System.StringComparer.Ordinal)");
            sb.AppendLine("                {");
            sb.AppendLine($"                    \"{flowOutPortName}\",");
            if (extraFlowOutputs != null)
            {
                foreach (var name in extraFlowOutputs)
                {
                    sb.AppendLine($"                    \"{name}\",");
                }
            }

            sb.AppendLine("                },");
            AppendDataOutputs(sb, dataOutputs);
            sb.AppendLine($"                EntryTypeId = typeof({payloadTypeFq}).AssemblyQualifiedName,");
            sb.AppendLine("            });");
            return sb.ToString();
        }

        internal static string BuildExecutableActionRegistrationBlock(
            string runnerFq,
            string editorTypeFq,
            string runtimeTypeFq,
            string flowInPortName,
            IReadOnlyList<(string Name, string CSharpType)> dataInputs)
        {
            var sb = new StringBuilder();
            sb.AppendLine($"            r.Register(new {EditorRegistryTypeName}<{runnerFq}>.NodeRegistration");
            sb.AppendLine("            {");
            sb.AppendLine($"                EditorNodeType = typeof({editorTypeFq}),");
            AppendInlineFactory(sb, editorTypeFq, runtimeTypeFq, dataInputs);
            sb.AppendLine("                FlowInputPortNames = new System.Collections.Generic.HashSet<string>(System.StringComparer.Ordinal)");
            sb.AppendLine("                {");
            sb.AppendLine($"                    \"{flowInPortName}\",");
            sb.AppendLine("                },");
            AppendDataInputs(sb, dataInputs);
            sb.AppendLine("            });");
            return sb.ToString();
        }

        internal static string BuildCommandRegistrationBlock(
            string runnerFq,
            string editorTypeFq,
            string runtimeTypeFq,
            string flowInPortName,
            string flowOutPortName,
            IReadOnlyList<(string Name, string CSharpType)> dataInputs,
            IReadOnlyList<string> dataOutputs)
        {
            var sb = new StringBuilder();
            sb.AppendLine($"            r.Register(new {EditorRegistryTypeName}<{runnerFq}>.NodeRegistration");
            sb.AppendLine("            {");
            sb.AppendLine($"                EditorNodeType = typeof({editorTypeFq}),");
            AppendInlineFactory(sb, editorTypeFq, runtimeTypeFq, dataInputs);
            sb.AppendLine("                FlowInputPortNames = new System.Collections.Generic.HashSet<string>(System.StringComparer.Ordinal)");
            sb.AppendLine("                {");
            sb.AppendLine($"                    \"{flowInPortName}\",");
            sb.AppendLine("                },");
            sb.AppendLine("                FlowOutputPortNames = new System.Collections.Generic.HashSet<string>(System.StringComparer.Ordinal)");
            sb.AppendLine("                {");
            sb.AppendLine($"                    \"{flowOutPortName}\",");
            sb.AppendLine("                },");
            AppendDataInputs(sb, dataInputs);
            AppendDataOutputs(sb, dataOutputs);
            sb.AppendLine("            });");
            return sb.ToString();
        }

        /// <summary>
        /// Build a registration block for a pure-data <c>[GraphNode]</c> or hand-written data node
        /// (no flow ports, no entry-type id).
        /// </summary>
        internal static string BuildDataNodeRegistrationBlock(
            string runnerFq,
            string editorTypeFq,
            string runtimeTypeFq,
            IReadOnlyList<(string Name, string CSharpType)> dataInputs,
            IReadOnlyList<string> dataOutputs)
        {
            var sb = new StringBuilder();
            sb.AppendLine($"            r.Register(new {EditorRegistryTypeName}<{runnerFq}>.NodeRegistration");
            sb.AppendLine("            {");
            sb.AppendLine($"                EditorNodeType = typeof({editorTypeFq}),");
            sb.AppendLine($"                Factory = _ => new {runtimeTypeFq}(),");
            AppendDataInputs(sb, dataInputs);
            AppendDataOutputs(sb, dataOutputs);
            sb.AppendLine("            });");
            return sb.ToString();
        }

        /// <summary>
        /// Build a registration block for a <c>[GraphNode]</c> flow node with explicit flow-in plus
        /// N flow-outs and arbitrary data ports.
        /// </summary>
        internal static string BuildFlowNodeRegistrationBlock(
            string runnerFq,
            string editorTypeFq,
            string runtimeTypeFq,
            IReadOnlyList<string> flowOutputs,
            IReadOnlyList<(string Name, string CSharpType)> dataInputs,
            IReadOnlyList<string> dataOutputs)
        {
            var sb = new StringBuilder();
            sb.AppendLine($"            r.Register(new {EditorRegistryTypeName}<{runnerFq}>.NodeRegistration");
            sb.AppendLine("            {");
            sb.AppendLine($"                EditorNodeType = typeof({editorTypeFq}),");
            sb.AppendLine($"                Factory = _ => new {runtimeTypeFq}(),");
            sb.AppendLine("                FlowInputPortNames = new System.Collections.Generic.HashSet<string>(System.StringComparer.Ordinal)");
            sb.AppendLine("                {");
            sb.AppendLine($"                    \"FlowIn\",");
            sb.AppendLine("                },");
            if (flowOutputs.Count > 0)
            {
                sb.AppendLine("                FlowOutputPortNames = new System.Collections.Generic.HashSet<string>(System.StringComparer.Ordinal)");
                sb.AppendLine("                {");
                foreach (var name in flowOutputs)
                {
                    sb.AppendLine($"                    \"{name}\",");
                }

                sb.AppendLine("                },");
            }

            AppendDataInputs(sb, dataInputs);
            AppendDataOutputs(sb, dataOutputs);
            sb.AppendLine("            });");
            return sb.ToString();
        }

        static void AppendInlineFactory(
            StringBuilder sb,
            string editorTypeFq,
            string runtimeTypeFq,
            IReadOnlyList<(string Name, string CSharpType)> dataInputs)
        {
            if (dataInputs.Count == 0)
            {
                sb.AppendLine($"                Factory = _ => new {runtimeTypeFq}(),");
                return;
            }

            sb.AppendLine("                Factory = node =>");
            sb.AppendLine("                {");
            sb.AppendLine($"                    var rt = new {runtimeTypeFq}();");
            sb.AppendLine($"                    var typed = ({editorTypeFq})node;");
            foreach (var f in dataInputs)
            {
                sb.AppendLine($"                    {{");
                sb.AppendLine($"                        var p = typed.GetInputPortByName({editorTypeFq}.{f.Name}PortName);");
                sb.AppendLine($"                        if (p != null && !p.isConnected && p.TryGetValue<{f.CSharpType}>(out var v))");
                sb.AppendLine($"                            rt.{f.Name} = v{NullableCoalesce(f.CSharpType)};");
                sb.AppendLine($"                    }}");
            }

            sb.AppendLine("                    return rt;");
            sb.AppendLine("                },");
        }

        static string NullableCoalesce(string csharpType)
        {
            return csharpType == "string" ? " ?? \"\"" : "";
        }

        static void AppendDataInputs(StringBuilder sb, IReadOnlyList<(string Name, string CSharpType)> dataInputs)
        {
            if (dataInputs.Count == 0)
            {
                return;
            }

            sb.AppendLine("                DataInputPortNames = new System.Collections.Generic.HashSet<string>(System.StringComparer.Ordinal)");
            sb.AppendLine("                {");
            foreach (var f in dataInputs)
            {
                sb.AppendLine($"                    \"{f.Name}\",");
            }

            sb.AppendLine("                },");
        }

        static void AppendDataOutputs(StringBuilder sb, IReadOnlyList<string> dataOutputs)
        {
            if (dataOutputs.Count == 0)
            {
                return;
            }

            sb.AppendLine("                DataOutputPortNames = new System.Collections.Generic.HashSet<string>(System.StringComparer.Ordinal)");
            sb.AppendLine("                {");
            foreach (var name in dataOutputs)
            {
                sb.AppendLine($"                    \"{name}\",");
            }

            sb.AppendLine("                },");
        }

        internal static string TrimGlobal(string fq)
        {
            return fq.StartsWith("global::", System.StringComparison.Ordinal) ? fq.Substring(8) : fq;
        }
    }
}
